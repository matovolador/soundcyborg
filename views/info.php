<h4>Bienvenidos a SoundCyborg.com</h4>
<p>De qu&eacute; trata el portal:</p>
<p>Básicamente el portal cuenta con dos secciones: 1) sección de avisos. 2) sección de música.</p>
<p>La sección de avisos es para que cualquiera que esté buscando algo relacionado a la música y personas, publique un aviso de forma gratuita en nuestro portal, y que podamos ser una fuente valiosa para el encuentro entre este tipo de personas. El portal cuenta con la funcionalidad de publicación de avisos y de búsqueda de avisos publicados.</p>
<p>La sección de música se divide en dos: 1)usuarios registrados 2)usuarios no registrados.</p>
<p>Los usuarios registrados pueden hacer las siguientes cosas:</p>
<ul>
<li>Subir hasta 20 canciones en formato mp3.</li>
<li>Subir una foto de perfil.</li>
<li>Dejar una descripción breve donde pueden incluir cualquier información mientras cumpla con nuestros términos de uso.</li>
<li>Comunicarse con otros usuarios registrados.</li>
</ul>
<p>Tanto los usuarios registrados como los no registrados, pueden escuchar la música subida por usuarios registrados en la sección "música", donde se puede filtrar por género y se ordena siempre en función de los "me gusta". Esta función de "me gusta" solo pueden utilizarla los usuarios registrados, suban música o no.</p>
<p>Todos estos servicios se brindan de forma gratuita.</p>
<p>El portal apunta exclusivamente al público y a los músicos latino americanos. Y sobre todo a los músicos sin producción ni sello discográfico, que recién arrancan su trayectoria, y tienen una grabación de buena o mala calidad que quieren compartir con otras personas de nuestra regi&oacute;n.</p>
<p>El cometido de la plataforma de música es lograr una base de datos masiva donde los latino americanos podamos encontrar y compartir la música fresca y emergente de nuestro territorio.</p>
<p>Los tipos de música admitidos se resumen al momento en el rock, todos los derivados o asociados principales del rock ( rock alternativo, blues, reggae, ska, etc) metal y electrónica.</p>
<p>Cualquier duda o consulta pueden contactarnos a trav&eacute;z del formulario de contacto, mandarnos un email a info@soundcyborg.com, o comuncarse a <a href="http://www.facebook.com/SoundCyborg" target="_blank" >nuestro facebook</a> por mensaje privado. Con gusto responderemos a cualquier consulta.</p>

